    fn unwrap_ok<T, E: core::fmt::Debug>(result: Result<T, E>) -> T {
        match result {
            Ok(value) => value,
            Err(error) => panic!("expected Ok(..), got Err({error:?})"),
        }
    }

    #[track_caller]
    fn unwrap_err<T, E: core::fmt::Debug>(result: Result<T, E>) -> E {
        match result {
            Ok(_) => panic!("expected Err(..), got Ok(..)"),
            Err(error) => error,
        }
    }

    fn toy_tensor(values: &[f32]) -> Tensor {
        unwrap_ok(Tensor::from_vec(1, values.len(), values.to_vec()))
    }

    fn toy_stage(interpolation: f32, objective: f32, density: &Tensor) -> BarycenterIntermediate {
        BarycenterIntermediate {
            interpolation,
            density: density.clone(),
            kl_energy: interpolation * 0.5,
            entropy: interpolation * 0.25,
            objective,
        }
    }

    fn toy_barycenter(density: &Tensor) -> ZSpaceBarycenter {
        ZSpaceBarycenter {
            density: density.clone(),
            kl_energy: 0.1,
            entropy: 0.3,
            coupling_energy: 0.0,
            objective: 0.4,
            effective_weight: 1.0,
            intermediates: vec![toy_stage(0.2, 0.6, density), toy_stage(0.8, 0.4, density)],
        }
    }

    #[test]
    fn homotopy_validates_finite_ingress_and_uses_a_common_commutator_seed() {
        let error = unwrap_err(HomotopyDifferential::new(
            toy_tensor(&[0.0, f32::NAN]),
            toy_tensor(&[1.0, 1.0]),
            toy_tensor(&[1.0, 1.0]),
        ));
        assert!(matches!(
            error,
            TensorError::NonFiniteValue {
                label: "homotopy_seed",
                ..
            }
        ));

        let first = unwrap_ok(HomotopyDifferential::new(
            toy_tensor(&[1.0, 2.0]),
            toy_tensor(&[0.5, -0.5]),
            toy_tensor(&[2.0, 2.0]),
        ));
        let second = unwrap_ok(HomotopyDifferential::new(
            toy_tensor(&[9.0, 9.0]),
            toy_tensor(&[0.25, 0.75]),
            toy_tensor(&[4.0, -2.0]),
        ));
        let commutator = unwrap_ok(first.commutator(&second, None));
        assert!(commutator.data().iter().all(|value| value.abs() < 1e-6));
    }

