    #[test]
    fn functor_uses_stable_matmul_and_exact_linearisation() {
        let cancellation = unwrap_ok(FunctorDifferential::new(
            toy_tensor(&[f32::MAX, f32::MAX]),
            unwrap_ok(Tensor::from_vec(2, 1, vec![2.0, -2.0])),
            0.1,
        ));
        assert_eq!(unwrap_ok(cancellation.apply()).data(), &[0.0]);

        let functor = unwrap_ok(FunctorDifferential::new(
            toy_tensor(&[1.0, 1.0]),
            unwrap_ok(Tensor::from_vec(2, 1, vec![4.0, 5.0])),
            f32::from_bits(1),
        ));
        let linear = unwrap_ok(functor.linearise(&toy_tensor(&[2.0, 3.0])));
        assert_eq!(linear.data(), &[23.0]);
    }

    #[test]
    fn recursive_differential_validates_curve_and_endpoint() {
        let density = toy_tensor(&[0.6, 0.4]);
        let barycenter = toy_barycenter(&density);
        assert!(RecursiveDifferential::from_barycenter(&barycenter).is_ok());

        let mut rising = barycenter.clone();
        rising.intermediates[1].objective = 0.7;
        let error = unwrap_err(RecursiveDifferential::from_barycenter(&rising));
        assert!(matches!(
            error,
            TensorError::InvalidValue {
                label: "recursive_objective_curve"
            }
        ));

        let mut wrong_endpoint = barycenter;
        wrong_endpoint.intermediates[1].density = toy_tensor(&[0.5, 0.5]);
        let error = unwrap_err(RecursiveDifferential::from_barycenter(&wrong_endpoint));
        assert!(matches!(
            error,
            TensorError::InvalidValue {
                label: "recursive_barycenter_endpoint"
            }
        ));
    }

    #[test]
    fn infinity_tower_validates_ingress_and_reduces_in_f64() {
        let error = unwrap_err(InfinityDifferential::new(
            vec![toy_tensor(&[1.0, 2.0]), toy_tensor(&[1.0])],
            vec![],
        ));
        assert!(matches!(error, TensorError::ShapeMismatch { .. }));
        let error = unwrap_err(InfinityDifferential::new(
            vec![toy_tensor(&[1.0])],
            vec![f32::NAN],
        ));
        assert!(matches!(
            error,
            TensorError::NonFiniteValue {
                label: "infinity_curvature",
                ..
            }
        ));

        let tower = unwrap_ok(InfinityDifferential::new(
            vec![toy_tensor(&[f32::MAX]), toy_tensor(&[f32::MAX])],
            vec![],
        ));
        assert_eq!(unwrap_ok(tower.collapse()).data(), &[f32::MAX]);

        let energy = unwrap_ok(InfinityDifferential::new(
            vec![toy_tensor(&[3.0, 4.0])],
            vec![-2.0],
        ));
        assert_eq!(
            unwrap_ok(energy.hierarchical_energy_tensor()).data(),
            &[50.0]
        );
    }

    #[test]
    fn spiral_differential_rejects_cross_layer_shape_and_volume_mismatch() {
        let build_parts = || {
            let seed = toy_tensor(&[0.6, 0.4]);
            let homotopy = unwrap_ok(HomotopyDifferential::new(
                seed.clone(),
                toy_tensor(&[0.1, 0.2]),
                toy_tensor(&[0.3, 0.4]),
            ));
            let functor = unwrap_ok(FunctorDifferential::new(
                seed.clone(),
                unwrap_ok(Tensor::from_vec(2, 2, vec![1.0, 0.0, 0.0, 1.0])),
                0.1,
            ));
            let recursive = unwrap_ok(RecursiveDifferential::from_barycenter(&toy_barycenter(
                &seed,
            )));
            (homotopy, functor, recursive)
        };

        let (homotopy, functor, recursive) = build_parts();
        let infinity = unwrap_ok(InfinityDifferential::new(vec![toy_tensor(&[1.0])], vec![]));
        let error = unwrap_err(SpiralDifferential::new(
            None, homotopy, functor, recursive, infinity,
        ));
        assert!(matches!(error, TensorError::ShapeMismatch { .. }));

        let (homotopy, functor, recursive) = build_parts();
        let infinity = unwrap_ok(InfinityDifferential::new(
            vec![toy_tensor(&[0.6, 0.4])],
            vec![],
        ));
        let topos = unwrap_ok(OpenCartesianTopos::new(-1.0, 1e-5, 10.0, 8, 1));
        let error = unwrap_err(SpiralDifferential::new(
            Some(topos),
            homotopy,
            functor,
            recursive,
            infinity,
        ));
        assert!(matches!(error, TensorError::TensorVolumeExceeded { .. }));
    }

    #[test]
    fn spiral_differential_resonates() {
        let seed = toy_tensor(&[0.5, -0.2]);
        let generator = toy_tensor(&[1.0, -1.0]);
        let direction = toy_tensor(&[0.1, 0.4]);
        let homotopy = unwrap_ok(HomotopyDifferential::new(
            seed.clone(),
            generator,
            direction,
        ));
        let kernel = unwrap_ok(Tensor::from_vec(2, 2, vec![1.0, 0.5, -0.5, 1.5]));
        let functor = unwrap_ok(FunctorDifferential::new(seed.clone(), kernel, 0.01));
        let density = toy_tensor(&[0.6, 0.4]);
        let stage_a = toy_stage(0.2, 0.6, &density);
        let stage_b = toy_stage(0.8, 0.4, &density);
        let barycenter = ZSpaceBarycenter {
            density: density.clone(),
            kl_energy: 0.1,
            entropy: 0.3,
            coupling_energy: 0.0,
            objective: 0.4,
            effective_weight: 1.0,
            intermediates: vec![stage_a, stage_b],
        };
        let recursive = unwrap_ok(RecursiveDifferential::from_barycenter(&barycenter));
        let infinity = unwrap_ok(InfinityDifferential::new(
            vec![density.clone(), density.clone()],
            vec![1.0, 2.0],
        ));
        let topos = unwrap_ok(OpenCartesianTopos::new(-1.0, 1e-4, 10.0, 8, 16));
        let spiral = unwrap_ok(SpiralDifferential::new(
            Some(topos),
            homotopy,
            functor,
            recursive,
            infinity,
        ));
        let resonance = unwrap_ok(spiral.resonate(None));
        assert_eq!(resonance.homotopy_flow.shape(), (1, 2));
        assert_eq!(resonance.functor_linearisation.shape(), (1, 2));
        assert_eq!(resonance.recursive_objective.shape().0, 1);
        assert_eq!(resonance.infinity_projection.shape(), (1, 2));
        assert_eq!(resonance.infinity_energy.shape().0, 1);
    }
